public class Contoh1 {
    public static void main(String [] args) {
        
        // perulangan For
        int x;
        for(x = 1; x <= 5; x++)
        {
            System.out.println("SMK Telkom Malang");
        }
        
        // Perulangan While
        int i = 1;
        while(i <= 5)
        {
            System.out.println("Smk Telkom Malang");
            i++;
        }
        
        // Perulangan DO While
        int j = 1;
        do
        {
            System.out.println("Smk Telkom Malang");
            j++;
        }
        while(j <= 5);
            
    }
    
}
